package Collections_Employee;

import java.util.Comparator;

public class Employee_Collections {
	private int age;
	private int id;
	private String name;
	public Employee_Collections(int age, int id, String name) {
		super();
		this.age = age;
		this.id = id;
		this.name = name;
	}
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
